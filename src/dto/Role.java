package dto;

public class Role {

	private int no;
	private String name;
	private int sort;

	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public int getSort() {
			return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
}
